use crate::custom_error::AocError;
use crate::grid_p2::Grid;
use crate::instruction::InstructionIter;

#[tracing::instrument]
pub fn process(input: &str) -> miette::Result<String, AocError> {
    let (grid_str, inst_str) = input.split_once("\n\n").unwrap();
    let mut grid = Grid::parse(grid_str);
    let instructions = InstructionIter::new(inst_str);
    for dir in instructions {
        grid.move_robot(dir);
    }
    Ok(format!("{}", grid.solve()))
}

